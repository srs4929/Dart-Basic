void main() {
  var num = 5;
  for (var i = 0; i < num; i++) {
    print("Learning dart");
  }
  //var in loop
  var names = ["Mango", "Orange", "Apple"];
  for (var name in names) {
    print(name);
  }
  //while loop
  while (num >= 1) {
    print(num);
    num--;
  }

  var x = ["Sumaiya","Rahman","Soma"];
  for (var y in x) {
    print(y);
  }
}
