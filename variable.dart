void main() {
  var x = 90;
  print(x);
  int y = 78;
  print(y);
  var name = "Soma";
  String n = "Rahman";
  print(name + " " + n);

  //dynamic
  dynamic p = "Tim";
  print(p);
  p = "Tom";
  print("Changed p " + p);

  //const and final
  const int t = 89;
  print(t);
  //declaring but not assigning
  var ok;
  print(ok);
}
