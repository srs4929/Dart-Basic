void main() {
  //String
  String x = "Soma";
  print("The name : $x");
  //Integer
  int y = 90;
  print("The value : $y");
  //Doubles
  double t = 90.97;
  print("The double : $t");

  //boolean
  bool mybool = true;
  print("My boolean value: $mybool");

  //dynamic
  dynamic test = "Ok";
  print("Test is :$test");
}
